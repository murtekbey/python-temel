# Object Oriented Programming (OOP)

# class
# Aşağıdaki gibi classlar bizim için python kütüphanesinde daha önce oluşturulmuş ve belli bağlı özellikler ve 
# metodlar bu aşağıdaki classların içerisine aktarılmış. bunlar hazır classlar
[] # list classı 
() # tuple classı
{} # dictionary classı

# instance (object)
# aşağıda ben bir lst oluşturduğum zaman aslında daha önceden var olan classın bir kopyasını bir objesini oluşturuyorum.
# aslında aşağı da gördüklerinizin hepsi list classı üzerinden oluşturulmuş bir kopya yani bir obje.
lst1 = [] # list classı 
lst2 = () # tuple classı
lst3 = {} # dictionary classı

# ' lst1. ' dediğim zaman karşıma çıkan metodlar aslında 'list' classı için üretilmiş olan metodlar ve bu şekilde bir kopyasını oluşturarak kullanabilirsin.

print(type(lst1)) # list classı
print(type(lst2)) # tuple classı
print(type(lst3)) # dictionary classı