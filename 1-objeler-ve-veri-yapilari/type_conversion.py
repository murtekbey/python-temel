x = 5               #int
y = 2.5             #float
name = 'Murat'      #string
isOnline = True     #bool

# Type Conversion

# int to float
x = float(x)
print(x)
print(type(x))

# float to int
y = int(y)
print(y)
print(type(y))

result = x + y
print(result)
print(type(result))

# bool to str
isOnline = str(isOnline)
print(isOnline)
print(type(isOnline))

# bool to int
isOnline = False
isOnline = int(isOnline)
print(isOnline)
print(type(isOnline))
